def plot_pred_vs_actual(y_true, y_pred, title='Baseline', rmse=None, r2=None):
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))

    lims = [min(y_true.min(), y_pred.min())-2, max(y_true.max(), y_pred.max())+2]
