    axes[0].plot(lims, lims, 'r--', linewidth=1.5, label='Perfect fit')
    axes[0].set_xlim(lims); axes[0].set_ylim(lims)
    axes[0].set_xlabel('Actual Strength (MPa)')
    axes[0].set_ylabel('Predicted Strength (MPa)')
